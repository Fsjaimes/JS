let a = 10;

console.log(a > 5);  //Devuleve un boolean
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

console.log(a == 10);  
console.log(a != 10); //Distinto de
console.log(a == '10'); //nos daria "true" porque los valores son iguales, sin embargo el tipo de dato es distinto 
console.log(a === '10'); //Para saber si es igual hasta en el tipo de dato usamos "===" 
console.log(a !== '10'); //Distinto tipo de dato o distinto tipo de valor